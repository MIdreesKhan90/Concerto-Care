  const app = Vue.createApp({
    data: function (){
      return{
        show: 1,
        step: 1,
        care_type: null,
        age: null,
        healthInsuranceSelected: '',
        findUsSelected: '',
        zipCodeFound: false,
        userZipCode: '',
        zipCodes: []
      }
    },
    computed: {

    },
    methods: {
      onChange(e) {
        var data = e.target.value;
        this.care_type = data;
        this.nextStep(2);

      },
      nextStep (step){
        this.step = step;
      },
      prevStep (step) {
        this.step = step;
      },
      zipCodeFiltered(){
        let self = this;
        let filtered = self.zipCodes.filter(function (item) {
          return item.zipCode === self.userZipCode;
        });
        if(filtered.length > 0){
          this.zipCodeFound = true;
          this.nextStep(3);
        }else{
          this.zipCodeFound = false;
          this.nextStep(3);
        }
      },
      healthInsuranceChange(e) {
        if(e.target.options.selectedIndex = 7) {
//          console.log(e.target.options[e.target.options.selectedIndex].dataset.foo)
          console.log('value found');
        }
      },
      findUsChange(e) {
        if(e.target.options.selectedIndex = 5) {
//          console.log(e.target.options[e.target.options.selectedIndex].dataset.foo)
          console.log('value found 2');
        }
      }
    },
    async mounted(){
        const res = await fetch('./assets/js/zipCode.json');
        const data = await res.json();
        this.zipCodes = data

  }
  });
  app.mount('#patientsEligibilityModal');
