                <p><strong>Q: Who should not get vaccinated? </strong></p>
                <p>A: You should not get the COVID-19 vaccine if you have had a severe allergic reaction (anaphylaxis) to any ingredient of the COVID-19 vaccine. If you have any concerns or questions about a possible allergic reaction, speak with your health care provider before getting the vaccine. You should also speak to your provider before getting the vaccine if you are immunocompromised, are pregnant or plan to become pregnant, or currently breastfeeding.  </p>

                <p><strong>Q: How does the COVID-19 vaccine work?</strong></p>
                <p>A: COVID-19 vaccines help our bodies develop immunity or protection against the virus that causes COVID-19 without us having to get sick with the virus. The vaccines contain a recipe for our own cells to manufacture harmless fragments of the virus that causes COVID-19. The body then recognizes these harmless fragments and builds an immune response and immune memory, helping your body recognize the virus if you are exposed or infected with the virus in the future. The vaccine does not contain any virus particles that cause COVID-19 (SARS-CoV-2) and <strong>cannot</strong> give you COVID-19.</p>

